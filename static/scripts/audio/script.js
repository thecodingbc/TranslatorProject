function updateCountry(selectLanguage, selectDialect, langs) {
    for (var i = selectDialect.options.length - 1; i >= 0; i--) {
        selectDialect.remove(i);
    }
    var list = langs[selectLanguage.selectedIndex];
    for (var i = 1; i < list.length; i++) {
        selectDialect.options.add(new Option(list[i][1], list[i][0]));
    }
    selectDialect.style.visibility = list[1].length == 1 ? 'hidden' : 'visible';
}

let select_language = document.querySelector('#select_language');
let select_dialect = document.querySelector('#select_dialect');
let select_language2 = document.querySelector('#select_language2');
let select_dialect2 = document.querySelector('#select_dialect2');

for (var i = 0; i < langs.length; i++) {
    select_language.options[i] = new Option(langs[i][0], i);
    
}
for (var i = 0; i < langs2.length; i++) {
    select_language2.options[i] = new Option(langs2[i][0], i);
}

select_language.selectedIndex = 0;
select_language2.selectedIndex = 0;
updateCountry(select_language, select_dialect, langs);
updateCountry(select_language2, select_dialect2, langs2);
select_dialect.selectedIndex = 0;
select_dialect2.selectedIndex = 0;